<script lang="ts">
	import useStyles from './Code.styles';
	import CopyIcon from './CopyIcon.svelte';
	import { get_current_component } from 'svelte/internal';
	import { clipboard, useActions, createEventForwarder } from '$lib/internal';
	import { CodeErrors } from './Code.errors';
	import Error from '$lib/internal/errors/Error.svelte';
	import type { CodeProps as $$CodeProps } from './Code.styles';

	export let use: $$CodeProps['use'] = [],
		element: $$CodeProps['element'] = undefined,
		className: $$CodeProps['className'] = '',
		override: $$CodeProps['override'] = {},
		color: $$CodeProps['color'] = 'gray',
		block: $$CodeProps['block'] = false,
		width: $$CodeProps['width'] = 100,
		copy: $$CodeProps['copy'] = false,
		message: $$CodeProps['message'] = 'Copied',
		noMono: $$CodeProps['noMono'] = false;
	export { className as class };

	/** An action that forwards inner dom node events from parent component */
	const forwardEvents = createEventForwarder(get_current_component());

	/** Copy logic */
	let copied = false;
	function toggle() {
		// sets the copied state for icon
		copied = true;
		setTimeout(() => (copied = false), 3000);
	}

	// --------------Error Handling-------------------
	let observable: boolean = false;
	let err;

	if (!block && width < 100) {
		observable = true;
		err = CodeErrors[0];
	}

	if (copy && message === 'Copied') {
		observable = true;
		err = CodeErrors[1];
	}

	if (copy && !block) {
		observable = true;
		err = CodeErrors[2];
	}

	$: if (observable) override = { display: 'none' };
	// --------------Error Handling-------------------

	$: ({ cx, getStyles } = useStyles({ color, block, noMono, width }));
</script>

<Error {observable} component="Code" code={err} />

<!--
@component
Inline or block code without syntax highlighting
	
@see https://svelteui.org/core/code
@example
    ```tsx
    <Code color='green' size='lg' variant='bars' /> // standard code component
    <Code color='blue' block copy message={codeToCopy} /> // code with block and copy props
    ```
-->

{#if block}
	<pre
		bind:this={element}
		use:useActions={use}
		use:forwardEvents
		class={cx(className, getStyles({ css: override }))}
		{...$$restProps}>
		{#if !noMono}
			<code class={className}><slot>Write some code</slot></code>
		{:else}
			<p class={className}><slot>Write some code</slot></p>
		{/if}
      {#if copy}
			<button on:click={toggle} use:clipboard={message} class:copy><CopyIcon {copied} /></button>
		{/if}
    </pre>
{:else}
	<code
		bind:this={element}
		use:useActions={use}
		use:forwardEvents
		class={cx(className, getStyles({ css: override }))}
		{...$$restProps}
	>
		<slot>Write some code</slot>
	</code>
{/if}

<style>
	button {
		border: 0;
		padding: 0;
		background: none;
		cursor: pointer;
	}
</style>
